package reactor;

public class Neut {
    int x;
    int y;
    double xv;
    double yv;
    int lifetime;
    public Neut(int x, int y, double xv, double yv, int lifetime){
        this.x = x;
        this.y = y;
        this.xv = xv;
        this.yv = yv;
        this.lifetime = lifetime;
    }
}
