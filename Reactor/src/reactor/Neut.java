package reactor;

public class Neut {
    double x;
    double y;
    double z;
    double xv;
    double yv;
    double zv;
    int lifetime;
    public Neut(double x, double y, double z, double xv, double yv, double zv, int lifetime){
        this.x = x;
        this.y = y;
        this.z = z;
        this.xv = xv;
        this.yv = yv;
        this.zv = zv;
        this.lifetime = lifetime;
    }
}
