package reactor;

public class Neut {
    double x;
    double y;
    double xv;
    double yv;
    int lifetime;
    public Neut(double x, double y, double xv, double yv, int lifetime){
        this.x = x;
        this.y = y;
        this.xv = xv;
        this.yv = yv;
        this.lifetime = lifetime;
    }
}
